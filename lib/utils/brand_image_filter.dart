
class BrandFilterUtils {
  static String idBrandToLogo(int id) {
    switch (id) {
      case 1:
        return "assets/images/audi logo.png";
        break;
      case 2:
        return "assets/images/chevrolet logo.png";
        break;
      case 3:
        return "assets/images/hyundai logo.png";
        break;
    }
  }
}
